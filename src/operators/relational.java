package operators;

public class relational {

	public static void main(String[] args) {
	    // Relational operators ( <, >, <=, >=, ==, != )
        // We use relational operators in conditional statements and looping
	    int x = 8, y = 4;
        System.out.println("x == y : " + (x == y));
        System.out.println("x != y : " + (x != y));
        System.out.println("x > y : " + (x > y));
        System.out.println("x < y : " + (x < y));
        System.out.println("x >= y : " + (x >= y));
        System.out.println("x <= y : " + (x <= y));
	}

}

package operators;

public class bitwise {

	public static void main(String[] args) {
		
		// Bitwise operators (&, |, ^, ~)
        int n = 16, m = 4;
        System.out.println("The result of AND operation: " + (n & m));
        System.out.println("The result of OR operation: " + (n | m));
        System.out.println("The result of XOR operation: " + (n ^ m));
        System.out.println("The result of NOT operation (~x1): " +(~n));


	}

}

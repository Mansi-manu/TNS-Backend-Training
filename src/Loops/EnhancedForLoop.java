package Loops;


// Demo for Enhanced For Loop with String Array
public class EnhancedForLoop {

    // Method to demonstrate enhanced for loop
    void displayFruits() {
        String[] fruits = {"Apple", "Banana", "Cherry", "Mango", "Orange"};

        System.out.println(" Enhanced For Loop Example with Strings ");

        // Using enhanced for loop to print array elements
        for (String fruit : fruits) {
            System.out.println("Fruit: " + fruit);
        }
    }

}